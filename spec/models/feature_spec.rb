require 'rails_helper'

describe Feature do
  describe '#rated?' do
    context 'feature was not rated' do
      it do
        pending 'To be implemented'
        expect(subject.rated?).to be_falsey
        fail
      end
    end

    context 'feature was rated' do
      it do
        pending 'To be implemented'
        expect(subject.rated?).to be_truthy
        fail
      end
    end
  end
end
